void Game::PrintText(const char* str) {
    int lines = 0;
    int chars = 0;
    int current = 0;

    while (str[current] != '\0') {
        if (lines < 30) {
            std::cout << str[current];
            chars += 1;
            if (str[current] == '\n' || (chars % 300 == 0 && chars != 0)) {
                lines += 1;
            }
            if (str[current] == '\n') {
                chars = 0;
            }
            current += 1;
        }
        else {
            PauseText();
            lines = 0;
        }
    }
}